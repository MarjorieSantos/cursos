// && - se os dois valores forem verdadeiros

if (2==2 && 3==4){
    document.write("True")
} else{
    document.write("false")
}

// || - se um dos valores for verdadeiro
if (2==2 || 3>4){
    document.write("True")
} else{
    document.write("false")
}

// ! inverte o resultado 
if (!( 3==4)){
    document.write("True")
} else{
    document.write("false")
}